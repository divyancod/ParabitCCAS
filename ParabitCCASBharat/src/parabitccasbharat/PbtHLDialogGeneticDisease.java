    @Override
    public void mouseClicked(MouseEvent e) {
        int row=disease.rowAtPoint(e.getPoint());
        String name=tablemodel.getValueAt(row,1).toString();
        System.out.println(name);
        String query="insert into typesofgeneticdisease(sno,DiseaseType) values (null,'"+name+"')";
        try
        {
            if(flag)
            {
                String[] listoptions = {"Add This Disease","Cancel"};
                String msg="This disease is not our master datebase.\nDo you want to add this to the master data base? Adding will reflect all over the India.";
                msg=msg+"\nBe sure to confirm the disease name as it will be checked by Testers";
                   int option = JOptionPane.showOptionDialog(this,msg,
                  "Confirm Final Save",            
                  JOptionPane.YES_NO_OPTION,
                  JOptionPane.WARNING_MESSAGE,
                  null,     
                  listoptions,
                 null
               );
                 if(option==0)
                 {
                db.stm.execute(query);
                query="select * from typesofgeneticdisease where DiseaseType='"+name+"'";
                db.rs1=db.stm1.executeQuery(query);
                if(db.rs1.next())
                    dashBoard.hlmodel.setGenDiss(db.rs1.getString("Sno"));
                 }else
                 {
                     return;
                 }
            }
            else
            {
               int opt=JOptionPane.showConfirmDialog(null, "Add this to the current Houselisting as Genetic Disease?", "Confirm Disease", JOptionPane.YES_NO_OPTION); 
               if(opt==0)
               {
                   query="select * from typesofgeneticdisease where DiseaseType='"+name+"'";
                    db.rs1=db.stm1.executeQuery(query);
                    if(db.rs1.next())
                     dashBoard.hlmodel.setGenDiss(db.rs1.getString("Sno"));
               }else
                   return;
            }
        }catch(Exception ex)
        {
            ex.printStackTrace();
        }
        dispose();
    }

