    public void setTable()
    {
        tablemodel.setRowCount(0);
        int i=0;
        String se=like.getText();
        String query="SELECT * FROM `typesofgeneticdisease` WHERE DiseaseType like '%"+se+"%'";
        try
        {
            db.rs1=db.stm.executeQuery(query);
            while(db.rs1.next())
            {
                Object ob[]={++i,db.rs1.getString("DiseaseType")};
                flag=false;
                tablemodel.addRow(ob);
            }
            if(i==0)
            {
                Object ob[]={++i,se};
                tablemodel.addRow(ob);
                flag=true;
            }
        }catch(Exception e)
        {
            e.printStackTrace();
        }
    }
